export const makeResultsString = (
  e: React.MouseEvent<HTMLButtonElement, MouseEvent>,
  guesses: string[],
  answer: string,
  maxGuesses: number,
  day: string
) => {
  e.currentTarget.innerText = "Copied";

  const stripGuess = guesses.map((a) =>
    a
      .toLowerCase()
      .trim()
      .replaceAll(/[^\w\s]/g, "")
  );

  const answerPosition = stripGuess.indexOf(
    answer
      .toLowerCase()
      .trim()
      .replaceAll(/[^\w\s]/g, "")
  );
  let resultString: string[] = [];

  if (answerPosition < 0)
    return `Rockbusters #${day}
    
    游뗻 游린 游린 游린 游린 游린
    https://rockbusters.lol/`;

  for (var i = 0; i < maxGuesses; i++) {
    if (resultString.includes("游릴")) {
      resultString.push("拘럺잺");
    } else {
      i == answerPosition ? resultString.push("游릴") : resultString.push("游린");
    }
  }
  return `Rockbusters #${day}
    
游뗽 ${resultString.join(" ")}
https://rockbusters.lol/`;
};
